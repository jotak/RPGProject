// -----------------------------------------------------------------
// GAME OBJECT
// -----------------------------------------------------------------
#include "GameObject.h"
#include "../Display/DisplayEngine.h"

// -----------------------------------------------------------------
// Name : GameObject
// -----------------------------------------------------------------
GameObject::GameObject()
{
}

// -----------------------------------------------------------------
// Name : ~GameObject
// -----------------------------------------------------------------
GameObject::~GameObject()
{
	FREE(m_pGeometry);
}

// -----------------------------------------------------------------
// Name : update
// -----------------------------------------------------------------
void GameObject::update(double delta)
{
	if (m_pGeometry != NULL) {
		m_pGeometry->update(delta);
	}
}

// -----------------------------------------------------------------
// Name : display
// -----------------------------------------------------------------
void GameObject::display()
{
	if (m_pGeometry != NULL) {
		m_pGeometry->display(m_pos, Color::white);
	}
}
