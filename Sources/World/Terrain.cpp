#include "Terrain.h"
#include "../Display/DisplayEngine.h"

// -----------------------------------------------------------------
// Name : Terrain
// -----------------------------------------------------------------
Terrain::Terrain()
{
}

// -----------------------------------------------------------------
// Name : ~Terrain
// -----------------------------------------------------------------
Terrain::~Terrain()
{
	FREEVEC(m_pGeometries);
}

// -----------------------------------------------------------------
// Name : display
// -----------------------------------------------------------------
void Terrain::display()
{
	for (list<Geometry*>::iterator it = m_pGeometries.begin(); it != m_pGeometries.end(); ++it) {
		(*it)->display(Coords3D(0, 0, BOARDPLANE), Color::white);
	}
}
